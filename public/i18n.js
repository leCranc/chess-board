let currentLang = 'en';

function detectLang() {
  const saved = localStorage.getItem('tournamentLang');
  if (saved && TRANSLATIONS[saved]) return saved;
  const nav = navigator.language || navigator.languages?.[0] || 'en';
  const code = nav.split('-')[0];
  return TRANSLATIONS[code] ? code : 'en';
}

function t(key) {
  return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS.en[key] || key;
}

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('tournamentLang', lang);
  document.documentElement.lang = lang;
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  // Dispatch event for dynamic content
  document.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
}

function initI18n() {
  currentLang = detectLang();
  // Build language selector
  const container = document.getElementById('lang-selector') || document.body;
  const existing = document.getElementById('lang-menu');
  if (existing) existing.remove();

  const menu = document.createElement('div');
  menu.id = 'lang-menu';
  menu.style.cssText = 'position:fixed;top:10px;right:10px;z-index:9999;display:flex;gap:4px;flex-wrap:wrap;';

  Object.entries(LANGUAGES).forEach(([code, lang]) => {
    const btn = document.createElement('button');
    btn.textContent = `${lang.flag} ${lang.name}`;
    btn.dataset.lang = code;
    btn.style.cssText = `padding:4px 10px;border-radius:6px;border:1px solid #3a3a5a;background:${code === currentLang ? '#2a2a4a' : '#16162e'};color:${code === currentLang ? '#e0e0ff' : '#888'};font-size:0.75rem;cursor:pointer;transition:all .2s;`;
    btn.onclick = () => {
      applyLang(code);
      document.querySelectorAll('#lang-menu button').forEach(b => {
        b.style.background = b.dataset.lang === code ? '#2a2a4a' : '#16162e';
        b.style.color = b.dataset.lang === code ? '#e0e0ff' : '#888';
      });
    };
    menu.appendChild(btn);
  });

  container.parentNode.insertBefore(menu, container);
  applyLang(currentLang);
}

// Auto-init on DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
